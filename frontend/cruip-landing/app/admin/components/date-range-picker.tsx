"use client"

import * as React from "react"
import { addDays, format } from "date-fns"
import { CalendarIcon } from "lucide-react"
import { DateRange } from "react-day-picker"

import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import { Calendar } from "@/components/ui/calendar"
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover"

type Props = Omit<React.HTMLAttributes<HTMLDivElement>, 'onChange'> & {
  value?: DateRange
  onChange?: (range: DateRange | undefined) => void
}

export function CalendarDateRangePicker({ className, value, onChange }: Props) {
  const [date, setDate] = React.useState<DateRange | undefined>(value || { from: undefined, to: undefined })

  React.useEffect(() => {
    if (value && (value.from !== date?.from || value.to !== date?.to)) setDate(value)
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [value?.from?.getTime?.(), value?.to?.getTime?.()])

  const handleSelect = (r?: DateRange) => {
    setDate(r)
    onChange?.(r)
  }

  return (
    <div className={cn("grid gap-2", className)}>
      <Popover>
        <PopoverTrigger asChild>
          <Button
            id="date"
            variant={"outline"}
            className={cn(
              "w-[260px] justify-start text-left font-normal",
              !date && "text-muted-foreground"
            )}
          >
            <CalendarIcon className="mr-2 h-4 w-4" />
            {date?.from ? (
              date.to ? (
                <>
                  {format(date.from, "LLL dd, y")} -{" "}
                  {format(date.to, "LLL dd, y")}
                </>
              ) : (
                format(date.from, "LLL dd, y")
              )
            ) : (
              <span>Pick a date</span>
            )}
          </Button>
        </PopoverTrigger>
        <PopoverContent className="w-auto p-0" align="end">
          <Calendar
            initialFocus
            mode="range"
            defaultMonth={date?.from}
            selected={date}
            onSelect={handleSelect}
            numberOfMonths={2}
          />
        </PopoverContent>
      </Popover>
    </div>
  )
}
